// PR description template for build auto-fix PRs

/**
 * Generate a rich PR description with failure analysis.
 * @param {object} opts
 * @param {string} opts.branch - Target branch
 * @param {number} opts.attempt - Current attempt number
 * @param {number} opts.maxAttempts - Max configured attempts
 * @param {string} opts.pipelineName - Pipeline/workflow name
 * @param {string|number} opts.runId - Failed run ID
 * @param {string} opts.runUrl - Link to the failed run
 * @param {string} opts.failedStep - Name of the failed step/job
 * @param {string} opts.errorSummary - Sanitized error summary (already redacted)
 * @param {string} opts.fixDescription - What the fix does
 * @param {string[]} opts.filesChanged - List of changed file paths
 * @param {string} opts.platform - "github" or "ado"
 * @returns {string} Markdown PR body
 */
export function buildPRDescription(opts) {
    const {
        branch = "unknown",
        attempt = 1,
        maxAttempts = 3,
        pipelineName = "unknown",
        runId = "unknown",
        runUrl = "",
        failedStep = "unknown",
        errorSummary = "No error details available.",
        fixDescription = "Auto-generated fix.",
        filesChanged = [],
        platform = "unknown",
    } = opts;

    const platformLabel = platform === "github" ? "GitHub Actions" : platform === "ado" ? "Azure DevOps" : platform;
    const runLink = runUrl ? `[Run #${runId}](${runUrl})` : `Run #${runId}`;
    const fileList = filesChanged.length > 0
        ? filesChanged.map((f) => `- \`${f}\``).join("\n")
        : "- _(no files listed)_";

    return `## 🔧 Auto-Fix: Build Failure on \`${branch}\` (Attempt ${attempt}/${maxAttempts})

### Failure Analysis
| Field | Value |
|-------|-------|
| **Platform** | ${platformLabel} |
| **Pipeline** | ${pipelineName} |
| **Run** | ${runLink} |
| **Failed Step** | ${failedStep} |

#### Error Summary
\`\`\`
${errorSummary.slice(0, 2000)}
\`\`\`

### Fix Applied
${fixDescription}

### Files Changed
${fileList}

---
> ⚠️ **This PR was generated by the build-autofix skill.**
> Requires human review and approval before merge.
> Do NOT auto-merge. Review all changes carefully.
`;
}

/**
 * Generate a concise PR title.
 */
export function buildPRTitle(branch, attempt) {
    return `fix(autofix): build failure on ${branch} [attempt ${attempt}]`;
}
